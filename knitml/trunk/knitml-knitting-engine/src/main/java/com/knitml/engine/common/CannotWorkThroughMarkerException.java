public class CannotWorkThroughMarkerException extends KnittingEngineException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public CannotWorkThroughMarkerException() {
		super();
	}

	public CannotWorkThroughMarkerException(String message, Throwable cause) {
		super(message, cause);
	}

	public CannotWorkThroughMarkerException(String message) {
		super(message);
	}

	public CannotWorkThroughMarkerException(Throwable cause) {
		super(cause);
	}

}
